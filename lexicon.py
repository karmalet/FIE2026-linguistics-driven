def find_predicate(text: str, hypothesis: str):
    """text에서 보문을 지배하는 핵심 谓词를 근사 식별.
    반환: (predicate, category, position)  category∈{factive,antifactive,double,neutral,drift,unknown}"""
    cands = []
    catlists = [
        ("double", DOUBLE), ("antifactive", ANTIFACTIVE), ("factive", FACTIVE),
        ("drift", DRIFT), ("neutral", NEUTRAL),
    ]
    for cat, lst in catlists:
        for w in lst:
            idx = text.find(w)
            while idx != -1:
                cands.append((idx, len(w), cat, w))
                idx = text.find(w, idx + 1)
    if not cands:
        return None, "unknown", -1
    # 보문이 hypothesis와 겹치는 정도가 큰(=뒤따르는 내용이 가설인) 谓词 우선, 동률이면 더 긴 단어, 그다음 앞쪽
    def overlap_after(pos, plen):
        comp = text[pos + plen:]
        return sum(1 for ch in set(hypothesis) if ch in comp)
    cands.sort(key=lambda c: (-overlap_after(c[0], c[1]), -c[1], c[0]))
    pos, plen, cat, w = cands[0]
    return w, cat, pos
